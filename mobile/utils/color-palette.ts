// Helper to get palette for mode
export const getPalette = (
  mode: "light" | "dark"
): Record<ColorName, ColorShades> => {
  return mode === "dark" ? DARK_PALETTE : LIGHT_PALETTE;
};

// Helper to check if color name is valid
export const isValidColor = (name: string): name is ColorName => {
  return [
    "blue",
    "green",
    "red",
    "purple",
    "orange",
    "pink",
    "teal",
    "indigo",
    "yellow",
  ].includes(name);
};
